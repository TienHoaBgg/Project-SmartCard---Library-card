    private void btn_ChangeInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ChangeInfoActionPerformed
        try {
            if (byteImg == null) {
                byteImg = new byte[0];
            }
            if (byteImg.length >= 32000) {
                JOptionPane.showMessageDialog(null, "Ảnh có kích thước quá lớn");
            } else {
                String fullName = VNCharacterUtils.removeAccent(txt_fullName.getText().trim());
                String khoa = VNCharacterUtils.removeAccent(txt_Khoa.getText().trim());
                String lop = VNCharacterUtils.removeAccent(txt_Lop.getText().trim());
                String masv = VNCharacterUtils.removeAccent(txt_maSV.getText().trim());
                if (fullName.isEmpty() || khoa.isEmpty() || lop.isEmpty() || masv.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Cần nhập đủ thông tin");
                } else {
                    info = new Infomation(fullName, khoa, lop, masv, byteImg);
                    if (card.saveInfo(info)) {
                        gui.readInfomation();
                        this.dispose();
                        gui.setVisible(true);
                        gui.validate();
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Đã có lỗi xảy ra");
        }

    }//GEN-LAST:event_btn_ChangeInfoActionPerformed

