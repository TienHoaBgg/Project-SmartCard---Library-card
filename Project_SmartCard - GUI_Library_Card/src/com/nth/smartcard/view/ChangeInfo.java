    private void btn_ChooseIMGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ChooseIMGActionPerformed
        JFileChooser file = new JFileChooser();
        file.setDialogTitle("Chọn ảnh sinh vien");
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg", "png");
        file.addChoosableFileFilter(filter);
        int result = file.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            try {
                File selectedFile = file.getSelectedFile();
                String path = selectedFile.getAbsolutePath();
                lb_img_avatar.setIcon(SmartCardWork.resizeImageIcon(path, lb_img_avatar));
                byteImg = Files.readAllBytes(selectedFile.toPath());

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Lỗi cover ảnh");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Chưa chọn được ảnh");
        }
    }//GEN-LAST:event_btn_ChooseIMGActionPerformed

